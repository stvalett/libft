#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	size_t			j;
	char			*ptr;

	if (!s || !(*f))
		return (0);
	j = ft_strlen(s);
	if ((ptr = (char *)malloc(sizeof(*ptr) * j + 1)) == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		ptr[i] = f(i, s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
